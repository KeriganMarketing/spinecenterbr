<p><strong>Add an Item</strong></p>
<ol>
    <li>Scroll to the bottom of the page.</li>
    <li>Under Add an Item, fill in all the information for the post. <br /><span class="small">(content editor instructions are at the bottom of this page)</span></li>
    <li>If there is a website for the post, add the url.<br /><span class="small">This must begin with "http://" to work correctly. See example below.</span><br /><img src="images/address-bar.jpg" /></li>
    <li>Enter the text that will link to the website from step 3.</li>
    <li>If you have a file for the post, you can upload it as well. <span class="small">This only works with PDFs or Word documents.</span></li>
  <li>Click “Submit” to publish your  post.</li>
</ol>
<p><strong>Edit an Item</strong></p>
<ol>
    <li>Click the title of the post you'd like to edit.</li>
    <li>Update the information for the post.</li>
    <li>If there is a website for the post, update the url.</li>
    <li>If you have a file for the post, you can upload it as well. If you upload a file to an post that already has a file, it will replace the original file.</li>
    <li>To delete the file from the post, click &quot;Delete the file currently associated with this item.&quot;</li>
    <li>Click “Submit” to publish your  updates.</li>
</ol>
<p><strong>Delete an Item</strong></p>
<ol>
  <li>Click the title of the post you'd like to delete.</li>
  <li>Scroll to the bottom of the page and click &quot;Delete.&quot;</li>
</ol>
