<h2>Photos</h2>
<p><strong>Add a Photo:</strong></p>
<ol>
  <li>Under &quot;Add an Image,&quot; click Browse and locate the file you'd like to upload from your computer.</li>
  <li>Specify a title and/or caption for the Image (optional).</li>
  <li>If your gallery has categories, select the category the image you are uploading should belong to.</li>
  <li>Click submit</li>
</ol>
<p><strong>Update a Photo:</strong></p>
<ol>
  <li>Find the preview for the image you'd like to update.</li>
  <li>In the preview box, click the Edit button. This will take you to a new page</li>
  <li>If you'd like to replace the image, click Browse, and choose the image you'd like to upload from your computer. Otherwise, leave this blank.</li>
  <li>Update the Title, Caption and (if available) Category to the correct values.</li>
  <li>Click Update.</li>
</ol>
<p><strong>Delete a Photo:</strong></p>
<ol>
  <li>Locate the preview of the image you'd like to delete.</li>
  <li>In the preview box, click Delete.</li>
</ol>
<p><strong>Change Photo Order:</strong></p>
<p>In each preview box, there is a field labeled &quot;sort order.&quot; The number in this box determines what order your photos are placed in. Update each images sort order by how you'd like them to appear (low to high), scroll to the bottom of the page and click &quot;Update Sort Order&quot; to update.</p>
