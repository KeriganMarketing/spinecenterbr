<h2>Categories<br />
<span class="small"><strong>NOTE: Not all galleries have categories. If your categories are used to specify images that appear in different parts of your site, you should consult with KMA before changing any Category settings</strong>.</span></h2>
<p><strong>Add a Category:</strong></p>
<ol>
<li>Click Manage your categories.</li>
  <li>Under Add a Category, input the name of the Category you'd like to add.</li>
</ol>
<p><strong>Update a Category:</strong></p>
<ol>
  <li>Click Manage your categories.</li>
  <li>Under Edit Current Categories, find the Category you'd like to update.</li>
  <li>Update the name of the Category.</li>
  <li>Click Update.</li>
</ol>
<p><strong>Delete a Category</strong>:</p>
<ol>
  <li>Click Manage your categories.</li>
  <li>Under Edit Current Categories, find the Category you'd like to delete.</li>
  <li>Click Delete.</li>
</ol>
