<p><strong>Add an Event</strong></p>
<ol>
    <li>Scroll to the bottom of the page.</li>
    <li>Under Add an Event, fill in all the information for the event. <br /><span class="small">(content editor instructions are at the bottom of this page)</span></li>
    <li>Choose whether the event is active or not. Inactive events will remain in the system but won't show on your site.</li>
    <li>If there is a website for the event, add the url.<br /><span class="small">This must begin with "http://" to work correctly. See example below.</span><br /><img src="images/address-bar.jpg" /></li>
    <li>If you have a flyer or registration form for the event, you can upload it as well. <br /><span class="small">This only works with PDFs or Word documents.</span></li>
  <li>Click “Submit” to publish your  event.</li>
</ol>
<p><strong>Edit an Event</strong></p>
<ol>
    <li>Click the title of the even you'd like to edit.</li>
    <li>Update the information for the event.</li>
    <li>Choose whether the event is active or not. Inactive events will remain in the system but won't show on your site.</li>
    <li>If there is a website for the event, update the url.</li>
    <li>If you have a flyer or registration form for the event, you can upload it as well. This only works with PDFs or Word documents. If you upload a file to an event that already has a file, it will replace the original file.</li>
    <li>To delete the file from the event, click &quot;Delete the file currently associated with this event.&quot;</li>
    <li>Click “Submit” to publish your  updates.</li>
</ol>
<p><strong>Delete an Event</strong></p>
<ol>
  <li>Click the title of the event you'd like to delete.</li>
  <li>Scroll to the bottom of the page and click &quot;Delete.&quot;</li>
</ol>
